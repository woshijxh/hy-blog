namespace App\Services;


use App\Exception\AuthException;
use App\Model\User;
use Hyperf\Di\Annotation\Inject;
use Phper666\JwtAuth\Jwt;

class AuthService
{
    /**
     * @Inject()
     * @var Jwt
     */
    public $jwt;

    /**
     * @param $validated
     */
    public function login($validated)
    {
        $userInfo = User::query()->where('username', $validated['username'])->first();
        if (!$userInfo) {
            throw new AuthException('账号不存在！');
        }

        if (!$this->verify($validated['password'], $userInfo->password)) {
            throw new AuthException('账号密码错误！');
        }

        $userInfo->last_time = time();
        $userInfo->save();

        return (string)$this->jwt->getToken([
            'id'       => $userInfo->id,
            'email'    => $userInfo->email,
            'username' => $userInfo->username,
        ]);
    }

    /**
     * @param $password
     * @param $truePassword
     * @return bool
     */
    public function verify($password, $truePassword)
    {
        return password_verify($password . 'jxh', $truePassword);
    }
}